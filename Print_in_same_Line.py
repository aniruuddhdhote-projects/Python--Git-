#When you use 2 print statements, Python By default uses next line to print the next statement
#But if you want to print line in the same line even if you are using multiple print statement 
#Use [end=" "] 

print('Without end=" " ')      #Note-We used single quotes here to print double quotes.
print ("I am a Human")
print ("My name is Aniruddh")

print('With end=" " ') 
print ("I am a Human",end=" ")   #Note- We add a space after end=" " for better readability.
print ("My name is Aniruddh")